test_that("encoding", {
  expect_equal(
    imb_encode("01", "234", "567094", "987654321", "01234567891"),
    "AADTFFDFTDADTAADAATFDTDDAAADDTDTTDAFADADDDTFFFDDTTTADFAAADFTDAADA"
  )
  expect_equal(
    imb_encode(1, 234, 567094, 987654321, "01234567891"),
    "AADTFFDFTDADTAADAATFDTDDAAADDTDTTDAFADADDDTFFFDDTTTADFAAADFTDAADA"
  )
})
